
a=input("enter the first the number :")
op=input("enter the operand +,-,*,/")
b=input("enter the second the number :")
a=int (a)
b=int (b)
if op=='+':
    print(a+b)
elif op=='-':
    print(a-b)
elif op=='*':
    print(a*b)
elif op=='/':
    print(a/b)
else:
    print("invalid operand")



